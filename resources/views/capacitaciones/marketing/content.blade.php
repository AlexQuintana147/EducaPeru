    // =========================
    // ACORDEÓN
    // =========================
    document.querySelectorAll('.accordion-header').forEach(header => {

        header.addEventListener('click', function() {

            const item = this.parentElement;
            const content = item.querySelector('.accordion-content');
            const icon = item.querySelector('.accordion-icon');

            document.querySelectorAll('.accordion-item').forEach(otherItem => {

                if (otherItem !== item && otherItem.classList.contains('active')) {

                    otherItem.classList.remove('active');

                    otherItem.querySelector('.accordion-content').style.maxHeight = '0';

                    // otherItem.querySelector('.accordion-icon').style.transform = 'rotate(0deg)';
                }

            });

            item.classList.toggle('active');

            if (item.classList.contains('active')) {

                content.style.maxHeight = content.scrollHeight + 'px';

                icon.style.transform = 'rotate(0deg)';

            } else {

                content.style.maxHeight = '0';

                // icon.style.transform = 'rotate(0deg)';
            }

        });

    });

    // INICIALIZAR PRIMER ACORDEÓN
    const firstItem = document.querySelector('.accordion-item.active');

    if (firstItem) {

        const content = firstItem.querySelector('.accordion-content');

        content.style.maxHeight = content.scrollHeight + 'px';
    }

    // =========================
    // ABRIR MODAL PDF
    // =========================
    function openPdfModal(pdfUrl)
    {
        const modal = document.getElementById('pdfModal');
        const viewer = document.getElementById('pdfViewer');

        viewer.src = pdfUrl + '#toolbar=1';

        modal.classList.remove('hidden');
        modal.classList.add('flex');
    }

    // =========================
    // CERRAR MODAL PDF
    // =========================
    function closePdfModal()
    {
        const modal = document.getElementById('pdfModal');
        const viewer = document.getElementById('pdfViewer');

        viewer.src = '';

        modal.classList.remove('flex');
        modal.classList.add('hidden');
    }

    // =========================
    // DESCARGAR PDF AUTOMÁTICO
    // =========================
    function downloadPdf(url, filename)
    {
        fetch(url)
            .then(response => response.blob())
            .then(blob => {

                const blobUrl = window.URL.createObjectURL(blob);

                const a = document.createElement('a');

                a.href = blobUrl;
                a.download = filename;

                document.body.appendChild(a);

                a.click();

                a.remove();

                window.URL.revokeObjectURL(blobUrl);
            });
    }

    // =========================
    // CERRAR MODAL AL HACER CLICK FUERA
    // =========================
    document.getElementById('pdfModal').addEventListener('click', function(e)
    {
        if(e.target === this)
        {
            closePdfModal();
        }
    });

</script>
